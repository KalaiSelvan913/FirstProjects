
public class Man1 {
	private String name;
	private int age;
	private String location;
	public Man1(String name, int age, String location){
		super();
		this.name = name;
		this.age = age;
		this.location = location;
		
	}
	public String getName() {
		return name;
	}
	public int getAge() {
		return age;
	}
	public String getLocation() {
		return location;
	}

}
